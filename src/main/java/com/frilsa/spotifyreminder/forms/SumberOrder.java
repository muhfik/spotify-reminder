    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String sql = "INSERT INTO sumber_order(kd_sumber, nama) VALUES (?,?)";

        try {
            PreparedStatement stat = database.getConnection().prepareStatement(sql);
            stat.setString(1, tfId.getText());
            stat.setString(2, tfNama.getText());

            stat.executeUpdate();

            searchData();
            JOptionPane.showMessageDialog(null, "Data Sumber Order Baru Telah Ditambahkan");
            
            clearInput();
            autonumber();
            tfNama.requestFocus();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Penambahan Data Paket Gagal! " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        String sql = "UPDATE sumber_order SET nama = ? WHERE kd_sumber = ?";
        try {
            PreparedStatement stat = database.getConnection().prepareStatement(sql);
            stat.setString(1, tfNama.getText());
            stat.setString(2, tfId.getText());
            stat.executeUpdate();

            searchData();
            JOptionPane.showMessageDialog(null, "Data Sumber Order Berhasil Diubah!");

            clearInput();
            autonumber();
            tfNama.requestFocus();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Sumber Order Gagal Diubah! " + e.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

