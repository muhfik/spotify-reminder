    private void searchData() {
        String cariItem = tfCari.getText();
        try {
            String sql = "SELECT * FROM sumber_order WHERE "
                    + "kd_sumber LIKE '%" + cariItem + "%' OR "
                    + "nama LIKE '%" + cariItem + "%' "
                    + "ORDER BY id ASC LIMIT 20";

            PreparedStatement ps = database.getConnection().prepareStatement(sql);
            ResultSet hasil = ps.executeQuery(sql);

            Object[] baris = {"ID", "Nama"};
            tabmode = new DefaultTableModel(null, baris);
            while (hasil.next()) {
                String id = hasil.getString("kd_sumber");
                String username = hasil.getString("nama");

                String[] data = {id, username};
                tabmode.addRow(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "[Code: 001] Terdapat kesalahan pada aplikasi, " + e.getMessage());
        }
        tblSumberOrder.setModel(tabmode);
    }

    private void autonumber() {
        int id = 0;
        try {
            String sql = "SELECT MAX(ID) AS 'last_id' FROM sumber_order";
            PreparedStatement stat = database.getConnection().prepareStatement(sql);
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {
                id = rs.getInt("last_id");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "[Code: 001] Terdapat kesalahan pada aplikasi, " + e.getMessage());
        }

        int u = id + 1;
        String ur;
        if (u < 10) {
            ur = "000" + u;
        } else if (u < 100) {
            ur = "00" + u;
        } else if (u < 1000) {
            ur = "0" + u;
        } else {
            ur = "" + u;
        }
        String kdPaket = "SO" + ur;
        tfId.setText(kdPaket);
    }

    private void clearInput() {
        tfId.setText("");
        tfNama.setText("");
    }

